class 자판기커피기계 {
	constructor(재료) {
		this.설탕 = 재료.설탕;
		this.원두 = 재료.원두;
		this.프림 = 재료.프림;
		this.밀크 = 재료.밀크;
	}

	충전하기() {
		this.설탕충전();
		this.원두충전();
		this.프림충전();
		this.밀크충전();
	}

	설탕충전() {
		this.설탕 = this.설탕 + 10;
	}

	원두충전() {
		this.원두 = this.원두 + 10;
	}

	프림충전() {
		this.프림 = this.프림 + 10;
	}

	밀크충전() {
		this.밀크 = this.밀크 + 10;
	}

	만들기() {}
}

const 자판기음료 = new 자판기커피기계({ 설탕: 100, 원두: 100, 프림: 100, 밀크: 100 });
console.log(자판기음료);
